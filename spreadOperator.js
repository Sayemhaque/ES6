const fruits = ["mangos", "guava", "banana", "apple"];

// spreading old array into an the new array and adding some new elements;
const newFruits = [...fruits, "orange", "pinapple"];

console.log(fruits); // output : [ 'mangos', 'guava', 'banana', 'apple' ]
console.log(newFruits); // output : [ 'mangos', 'guava', 'banana', 'apple', 'orange', 'pinapple' ]
